#include "client.h"
#include "crypt.h"

void CryptData(uint8 * ptr, uint32 len) {
	int ind = 0;

	while (len > 4) {
		*(uint32 *)ptr ^= CD_32[ind];
		ptr += 4;
		len -= 4;
		ind++;
		if (CD_32[ind] == 0) {
			ind = 0;
		}
	}

	ind = 0;
	while (len > 2) {
		*(uint16 *)ptr ^= CD_16[ind];
		ptr += 2;
		len -= 2;
		ind++;
		if (CD_16[ind] == 0) {
			ind = 0;
		}
	}

	ind = 0;
	while (len) {
		*(uint8 *)ptr ^= CD_8[ind];
		ptr++;
		len--;
		ind++;
		if (CD_8[ind] == 0) {
			ind = 0;
		}
	}

}

bool LoadConfig() {
#ifdef WIN32
	char fn[MAX_PATH];
	if (SHGetFolderPathA(NULL, CSIDL_APPDATA, NULL, SHGFP_TYPE_CURRENT, fn) == S_OK) {
		if (fn[strlen(fn)-1] != PATH_SEP) {
			strcat(fn, PATH_SEPS);
		}
	} else {
		fn[0] = 0;
	}
	strcat(fn, "ircbot_client3.dat");
#else
	char fn[MAX_PATH];
	char * p = getenv("HOME");
	strcpy(fn, p);
	if (fn[strlen(fn)-1] != PATH_SEP) {
		strcat(fn, PATH_SEPS);
	}
	strcat(fn, ".ircbot_client3.conf");
	printf("FN: %s\n", fn);
#endif

	FILE * fp = fopen(fn, "rb");
	if (fp == NULL) { fp = fopen("client3.dat", "rb"); }
	if (!fp) { return false; }

	fseek(fp, 0, SEEK_END);
	long len = ftell(fp);
	fseek(fp, 0, SEEK_SET);

	if (len == sizeof(CONFIG_OLD3)) {
		CONFIG_OLD3 cfg;
		fread(&cfg, sizeof(cfg), 1, fp);
		fclose(fp);
		strcpy(config.hosts[0].host, cfg.host);
		strcpy(config.hosts[0].pass, cfg.pass);
		strcpy(config.hosts[0].username, cfg.username);
		config.hosts[0].port = cfg.port;
		config.beep_on_req = true;
		return true;
	}

	if (len <= sizeof(CONFIG_OLD2)) {
		CONFIG_OLD2 cfg;
		fread(&cfg, 1, sizeof(cfg), fp);
		fclose(fp);
		for (int i=0; i < 16; i++) {
			strcpy(config.hosts[i].pass, cfg.pass);
			strcpy(config.hosts[i].username, cfg.username);
			strcpy(config.hosts[i].host, cfg.hosts[i].host);
			config.hosts[i].port = cfg.hosts[i].port;
		}
		config.index = cfg.index;
		config.beep_on_req = true;
		return true;
	}

	if (len == sizeof(CONFIG_OLD)) {
		fread(&config, sizeof(CONFIG_OLD), 1, fp);
		fclose(fp);
		CryptData((uint8 *)&config, sizeof(CONFIG_OLD));
		if (config.revision != 0x04) {
			memset(&config, 0, sizeof(config));
			wxMessageBox(_("Unknown config file revision!\n"), _("Error"));
			return false;
		} else {
			config.beep_on_req = true;
			config.keep_on_top = false;
		}
		return true;
	}

	if (len == sizeof(config)) {
		fread(&config, sizeof(config), 1, fp);
		fclose(fp);
		CryptData((uint8 *)&config, sizeof(config));
		if (config.revision != 0x05) {
			memset(&config, 0, sizeof(config));
			wxMessageBox(_("Unknown config file revision!\n"), _("Error"));
			return false;
		}
		return true;
	}

	fclose(fp);
	wxMessageBox(_("Unknown config file format!\n"), _("Error"));
	return false;
}

void SaveConfig() {
	CONFIG cfg;
	memset(&cfg, 0, sizeof(cfg));
	if (!memcmp(&config, &cfg, sizeof(config))) {
		//don't bother writing if it's all zeros
		return;
	}

#ifdef WIN32
	char fn[MAX_PATH];
	if (SHGetFolderPathA(NULL, CSIDL_APPDATA, NULL, SHGFP_TYPE_CURRENT, fn) == S_OK) {
		if (fn[strlen(fn)-1] != PATH_SEP) {
			strcat(fn, PATH_SEPS);
		}
	} else {
		fn[0] = 0;
	}
	strcat(fn, "ircbot_client3.dat");
#else
	char fn[MAX_PATH];
	char * p = getenv("HOME");
	strcpy(fn, p);
	if (fn[strlen(fn)-1] != PATH_SEP) {
		strcat(fn, PATH_SEPS);
	}
	strcat(fn, ".ircbot_client3.conf");
	printf("FN: %s\n", fn);
#endif

	FILE * fp = fopen(fn, "wb");
	if (fp) {
		config.revision = 0x05;
		CONFIG cfg;
		memcpy(&cfg, &config, sizeof(config));
		CryptData((uint8 *)&cfg, sizeof(cfg));
		fwrite(&cfg, sizeof(cfg), 1, fp);
		fclose(fp);
	} else {
		wxMessageBox(_("Error opening config file for write!\n"), _("Error"));
	}
}

